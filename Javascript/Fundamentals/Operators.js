// Javascript Fundamentals

// Operators: Arithmetic, Assignment, Comparison, Logical 

// Arithmetic
console.log(3 + 4, 2 ** 3);      // 7, 8

// Assignment
let x = 5;
x += 2;                           // 7

// Comparison
console.log("5" == 5);           // true
console.log("5" === 5);          // false

// Logical
console.log(true && false);      // false
console.log(false || true);      // true